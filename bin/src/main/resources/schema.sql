-- AUTH101: Configuration Table
CREATE TABLE IF NOT EXISTS AUTH101 (
    PROGRAM_ID VARCHAR(50) PRIMARY KEY,
    PROGRAM_NAME VARCHAR(255) NOT NULL,
    APPROVAL_REQ BOOLEAN DEFAULT TRUE,
    PRE_APPROVE_PROC BOOLEAN DEFAULT FALSE,
    PRE_EXEC_METHOD VARCHAR(1), -- '1': SQL, '2': API, '3': JAVA
    PRE_PROCESS_NAME VARCHAR(255),
    POST_APPROVE_PROC BOOLEAN DEFAULT FALSE,
    POST_EXEC_METHOD VARCHAR(1),
    POST_PROCESS_NAME VARCHAR(255),
    IS_TRAN BOOLEAN DEFAULT TRUE,
    AUTH_LEVELS INT DEFAULT 1
);

-- AUTH001: Authorization Queue Metadata
CREATE TABLE IF NOT EXISTS AUTH001 (
    ORG_CODE VARCHAR(10) NOT NULL,
    PROGRAM_ID VARCHAR(50) NOT NULL,
    AUTH_SL BIGINT PRIMARY KEY AUTO_INCREMENT,
    DISPLAY_REMARKS VARCHAR(MAX),
    FLUSER INT DEFAULT 0, -- 0: Pending, 1: Approved, 2: Rejected, 3: Correction
    FLUSER_ID VARCHAR(50),
    FLUSER_DATE TIMESTAMP,
    SLUSER INT DEFAULT 0,
    SLUSER_ID VARCHAR(50),
    SLUSER_DATE TIMESTAMP,
    TLUSER INT DEFAULT 0,
    TLUSER_ID VARCHAR(50),
    TLUSER_DATE TIMESTAMP,
    ENTRY_USER VARCHAR(50) NOT NULL,
    ENTRY_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- AUTH002: Authorization Data Blocks
CREATE TABLE IF NOT EXISTS AUTH002 (
    ORGCODE VARCHAR(25),
    EFFDATE DATE,
    PROGRAMID VARCHAR(50),
    PRIMARYKEY VARCHAR(500),
    AUTHSL BIGINT NOT NULL,
    RECSL INT NOT NULL,
    TABLENAME VARCHAR(50),
    DATABLOCK CLOB, -- Stores the record data as JSON string
    PRIMARY KEY (AUTHSL, RECSL)
);

-- USERS001: User Master Table
CREATE TABLE IF NOT EXISTS USERS001 (
    ORGCODE INT,
    USERSCD VARCHAR(50) PRIMARY KEY,
    MENUTYPE INT, -- 1-Rolewise, 2-Userwise
    GENDER VARCHAR(10),
    TITLE VARCHAR(10),
    FNAME VARCHAR(100),
    MNAME VARCHAR(100),
    LNAME VARCHAR(100),
    EMAIL VARCHAR(255),
    MOBILE VARCHAR(20),
    COUNTRY VARCHAR(2), -- 2 Digit country code
    EUSER VARCHAR(50),
    EDATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    AUSER VARCHAR(50),
    ADATE TIMESTAMP,
    CUSER VARCHAR(50),
    CDATE TIMESTAMP,
    CLIENTCD VARCHAR(50)
);

